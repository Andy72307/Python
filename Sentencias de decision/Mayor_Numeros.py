num1 = int(input("Ingrese el primer numero entero: "))
num2 = int(input("Ingrese el segundo numero entero: "))

if num1 > num2:
    print(f'El numero uno ({num1}) es mayor que el numero 2 ({num2})')
elif num1 < num2:
    print(f'El numero uno ({num1}) es menor que el numero 2 ({num2})')
else:
    print(f'El numero uno ({num1}) es igual que el numero 2 ({num2})')

