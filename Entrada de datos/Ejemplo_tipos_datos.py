#Ejemplo tipos datos

# Ejemplo de concatenacion o suma de valores
n1_cadena = '10'
print(f'Número 1 en cadena: {n1_cadena}')
n2_cadena = '20'
print(f'Número 2 en cadena: {n2_cadena}')
resultado = n1_cadena + n2_cadena
print(f'Concatenacion: {resultado}')

#Convertimos a tipos enteros
n1_entero = int(n1_cadena)
n2_entero = int(n2_cadena)
resultado = n1_entero + n2_entero
print(f'Suma: {resultado}')
