#Operadores de comparacion (Relacionales)
a, b = 5, 5
print(f'El valor inicial de a: {a} b: {b}')

#Operador de igualdad ==
c = a == b
print(f'Resultado a == b es: {c}')

#Operador diferente !=
c = a != b
print(f'Resultado a != b es {c}')

#Operador mayor que >
c = a > b
print(f'Resultado a > b es {c}')

#Operador mayor o igual que >=
c = a >= b
print(f'Resultado a >= b es {c}')

#Operador menor que <
c = a < b
print(f'Resultado a < b es {c}')

#Operador menor o igual que <=
c = a <= b
print(f'Resultado a <= b es {c}')