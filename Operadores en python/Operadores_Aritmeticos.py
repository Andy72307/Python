#Operadores Aritmeticos
A = 10
B = 3

#Suma +
C = A + B
print(f'La suma es: {C}')

#Resta -
C = A - B
print(f'La resta es: {C}')

#Multiplicacion *
C = A * B
print(f'La multiplicacion es: {C}')

#Division / (Retorna un tipo float)
C = A / B
print(f'La division es: {C:.2f}')

#Division entera // (Retorna un tipo int)
C = A // B
print(f'La division entera es: {C}')

#Modulo %
C = A % B
print(f'El Residuo de la division es: {C}')

#Exponente **
C = A ** B
print(f'El exponente es: {C}')