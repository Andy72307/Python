#Operadores de asignacion compuestos
a, b = 10, 15
print(f'Valor inicial de a: {a} b: {b}')

#Operador compuesto de +=
a += b # a = a + b
print(f'El operador de a += b es: {a}')

#Operador compuesto de -=
a = 10 #Reiniciando la variable a
a -= b # a = a - b
print(f'El operador de a -= b es: {a}')

#Operador compuesto *=
a = 10 #Reiniciando la variable a
a *= b # a = a * b
print(f'El operador de a *= b es: {a}')

#Operador compuesto /=
a = 10 #Reiniciando la variable a
a /= b # a = a * b
print(f'El operador de a /= b es: {a}')

#Operador compuesto %=
a = 10 #Reiniciando la variable a
a %= b # a = a * b
print(f'El operador de a %= b es: {a}')

#Operador compuesto **=
a = 10 #Reiniciando la variable a
a **= b # a = a * b
print(f'El operador de a **= b es: {a}')